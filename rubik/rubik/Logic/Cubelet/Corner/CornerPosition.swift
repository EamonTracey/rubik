enum CornerPosition: UInt8, CaseIterable {
    /*
     * There are 8 corner positions.
     */
    
    case upRightFront = 0
    case upRightBack = 1
    case upLeftFront = 2
    case upLeftBack = 3
    case downRightFront = 4
    case downRightBack = 5
    case downLeftFront = 6
    case downLeftBack = 7
}
